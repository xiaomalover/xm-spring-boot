<script>
    import {
        initDepartment,
        loadDepartment,
        getUserListData,
        getAllRoleList,
        addUser,
        editUser,
        enableUser,
        disableUser,
        deleteUser,
        updateAdminPassword
    } from "@/api/index";
    import moment from 'moment';

    export default {
        name: "user-manage",
        data() {
            const validatePassword = (rule, value, callback) => {
                var reg = /(?=.*[A-Z])(?=.*[a-z])(?=.*[0-9])[A-Za-z0-9]/;
                if (!reg.test(value) || value.length < 6 || value.length > 16) {
                    callback(new Error("密码必须6-16位大小写字母和数字组合"));
                } else {
                    callback();
                }
            };

            const validateMobile = (rule, value, callback) => {
                var reg = /^[1][3,4,5,7,8][0-9]{9}$/;
                if (!reg.test(value)) {
                    callback(new Error("手机号格式错误"));
                } else {
                    callback();
                }
            };

            const validateConfirmPassword = (rule, value, callback) => {
                if (value == '') {
                    callback(new Error('确认密码不能为空'));
                } else if (this.passwordForm.password != value) {
                    callback(new Error('确认密码错误'));
                } else {
                    callback();
                }
            };

            return {
                accessToken: {},
                loading: true,
                drop: false,
                dropDownContent: "展开",
                dropDownIcon: "ios-arrow-down",
                selectCount: 0,
                selectList: [],
                department: [],
                selectDep: [],
                dataDep: [],
                permTypes: [],
                searchForm: {
                    username: "",
                    departmentId: "",
                    mobile: "",
                    email: "",
                    sex: "",
                    type: "",
                    status: "",
                    pageNumber: 1,
                    pageSize: 10,
                    sort: "createdAt",
                    order: "desc",
                    startDate: "",
                    endDate: "",
                },
                selectDate: null,
                modalType: 0,
                userModalVisible: false,
                passwordModalVisible: false,
                modalTitle: "",
                passwordModalTitle: "修改密码",
                userForm: {
                    sex: 1,
                    type: 0,
                    roles: [],
                    password: "",
                    username: "",
                    email: "",
                    mobile: "",
                    nickname: "",
                    departmentId: "",
                    departmentTitle: ""
                },
                passwordForm: {
                    password: "",
                    confirmPassword: "",
                    id: 0
                },
                userRoles: [],
                roleList: [],
                errorPass: "",
                userFormValidate: {
                    username: [
                        {required: true, message: "账号不能为空", trigger: "blur"}
                    ],
                    mobile: [
                        {required: true, message: "手机号不能为空", trigger: "blur"},
                        {validator: validateMobile, trigger: "blur"}
                    ],
                    email: [
                        {required: true, message: "请输入邮箱地址"},
                        {type: "email", message: "邮箱格式不正确"}
                    ],
                    password: [
                        {required: true, message: "密码不能为空"},
                        {validator: validatePassword, trigger: "blur"},
                    ]
                },
                passwordFormValidate: {
                    password: [
                        {required: true, message: "密码不能为空", trigger: "blur"},
                        {validator: validatePassword, trigger: "blur"}
                    ],
                    confirmPassword: [
                        {validator: validateConfirmPassword, trigger: "blur"}
                    ],
                },
                submitLoading: false,
                columns: [
                    {
                        type: "selection",
                        width: 60,
                        align: "center",
                        fixed: "left"
                    },

                    {
                        title: "头像",
                        key: "avatar",
                        align: "center",
                        minWidth: 90,
                        render: (h, params) => {
                            if (params.row.avatar) {
                                return h('div', {
                                    attrs: {
                                        style: ''
                                    },
                                }, [
                                    h('img', {
                                        props: {
                                            type: 'primary',
                                            size: 'small'
                                        },
                                        attrs: {
                                            src: params.row.avatar,
                                            style: 'width: 100%; border-radius: 2px;'
                                        },
                                        style: "text-align:center;margin:10px auto;",
                                    }),
                                ]);
                            } else {
                                return h('div', '-');
                            }
                        }
                    },

                    {
                        title: "用户名",
                        key: "username",
                        minWidth: 100,
                    },
                    {
                        title: "昵称",
                        key: "nickname",
                        minWidth: 100,
                    },
                    {
                        title: "所属部门",
                        key: "departmentTitle",
                        minWidth: 120,
                    },
                    {
                        title: "手机",
                        key: "mobile",
                        minWidth: 120,
                    },
                    {
                        title: "邮箱",
                        key: "email",
                        minWidth: 120,
                    },
                    {
                        title: "性别",
                        key: "sex",
                        minWidth: 70,
                        align: "center",
                        render: (h, params) => {
                            let re = "";
                            if (params.row.sex === 1) {
                                re = "男";
                            } else if (params.row.sex === 0) {
                                re = "女";
                            }
                            return h("div", re);
                        }
                    },
                    {
                        title: "用户类型",
                        key: "type",
                        align: "center",
                        minWidth: 100,
                        render: (h, params) => {
                            let re = "";
                            if (params.row.type === 1) {
                                re = "管理员";
                            } else if (params.row.type === 0) {
                                re = "普通用户";
                            }
                            return h("div", re);
                        }
                    },
                    {
                        title: "状态",
                        key: "status",
                        align: "center",
                        minWidth: 120,
                        render: (h, params) => {
                            let re = "";
                            if (params.row.status === 1) {
                                return h("div", [
                                    h(
                                        "Tag",
                                        {
                                            props: {
                                                type: "dot",
                                                color: "success"
                                            }
                                        },
                                        "正常启用"
                                    )
                                ]);
                            } else if (params.row.status === 0) {
                                return h("div", [
                                    h(
                                        "Tag",
                                        {
                                            props: {
                                                type: "dot",
                                                color: "error"
                                            }
                                        },
                                        "禁用"
                                    )
                                ]);
                            }
                        },
                        filters: [
                            {
                                label: "正常启用",
                                value: 1
                            },
                            {
                                label: "禁用",
                                value: 0
                            }
                        ],
                        filterMultiple: false,
                        filterMethod(value, row) {
                            if (value === 1) {
                                return row.status === 1;
                            } else if (value === 0) {
                                return row.status === 0;
                            }
                        }
                    },
                    {
                        title: "创建时间",
                            key: "createdAt",
                            minWidth: 150,
                            sortable: true,
                            sortType: "desc",
                            render: (h, params) => {
                            return h("div", moment(params.row.createdAt * 1000).format('YYYY-MM-DD HH:mm:ss'));
                        }
                    },
                    {
                        title: "操作",
                        key: "action",
                        width: 270,
                        align: "center",
                        fixed: "right",
                        render: (h, params) => {
                            let editBtn; let disableBtn; let enableBtn; let deleteBtn; let updatePasswordBtn;
                            if (this.permTypes.includes("edit")) {
                                editBtn = h(
                                    "Button",
                                    {
                                        props: {
                                            type: "primary",
                                            size: "small"
                                        },
                                        style: {
                                            marginRight: "5px"
                                        },
                                        on: {
                                            click: () => {
                                                this.edit(params.row);
                                            }
                                        }
                                    },
                                    "编辑"
                                );
                            }

                            if (this.permTypes.includes("enable")) {
                                enableBtn = h(
                                    "Button",
                                    {
                                        props: {
                                            type: "success",
                                            size: "small"
                                        },
                                        style: {
                                            marginRight: "5px"
                                        },
                                        on: {
                                            click: () => {
                                                this.enable(params.row);
                                            }
                                        }
                                    },
                                    "启用"
                                );
                            }

                            if (this.permTypes.includes("disable")) {
                                disableBtn = h(
                                    "Button",
                                    {
                                        props: {
                                            size: "small"
                                        },
                                        style: {
                                            marginRight: "5px"
                                        },
                                        on: {
                                            click: () => {
                                                this.disable(params.row);
                                            }
                                        }
                                    },
                                    "禁用"
                                );
                            }

                            if (this.permTypes.includes("delete")) {
                                deleteBtn = h(
                                    "Button",
                                    {
                                        props: {
                                            type: "error",
                                            size: "small"
                                        },
                                        on: {
                                            click: () => {
                                                this.remove(params.row);
                                            }
                                        }
                                    },
                                    "删除"
                                );
                            }

                            if (this.permTypes.includes("updatePassword")) {
                                updatePasswordBtn = h(
                                    "Button",
                                    {
                                        props: {
                                            type: "warning",
                                            size: "small"
                                        },
                                        style: {
                                            marginRight: "5px"
                                        },
                                        on: {
                                            click: () => {
                                                this.updatePassword(params.row);
                                            }
                                        }
                                    },
                                    "修改密码"
                                );
                            }

                            if (params.row.status === 1) {
                                return h("div", [
                                    editBtn,
                                    disableBtn,
                                    updatePasswordBtn,
                                    deleteBtn,
                                ]);
                            } else {
                                return h("div", [
                                    editBtn,
                                    enableBtn,
                                    updatePasswordBtn,
                                    deleteBtn,
                                ]);
                            }
                        }
                    }
                ],
                data: [],
                exportData: [],
                total: 0
            };
        },
        methods: {
            init() {
                this.accessToken = {
                    accessToken: this.getStore("accessToken")
                };
                this.initDepartmentData();
                this.getUserList();
                this.getParentList();
            },
            initDepartmentData() {
                initDepartment().then(res => {
                    if (res.success === true) {
                        res.result.forEach(function (e) {
                            if (e.isParent) {
                                e.value = e.id;
                                e.label = e.title;
                                e.loading = false;
                                e.children = [];
                            } else {
                                e.value = e.id;
                                e.label = e.title;
                            }
                        });
                        this.deleteDisableNode(res.result);
                        this.department = res.result;
                    }
                });
            },

            getParentList() {
                initDepartment(0).then(res => {
                    if (res.success === true) {
                        res.result.forEach(function (e) {
                            if (e.isParent) {
                                e.loading = false;
                                e.children = [];
                            }
                        });
                        this.deleteDisableNode(res.result);
                        this.dataDep = res.result;
                    }
                });
            },

            // 递归标记禁用节点
            deleteDisableNode(permData) {
                let that = this;
                permData.forEach(function (e) {
                    if (e.status === 0) {
                        e.title = "[已禁用] " + e.title;
                        e.disabled = true;
                    }
                    if (e.children && e.children.length > 0) {
                        that.deleteDisableNode(e.children);
                    }
                });
            },
            loadData(item, callback) {
                item.loading = true;
                loadDepartment(item.value).then(res => {
                    item.loading = false;
                    if (res.success === true) {
                        res.result.forEach(function (e) {
                            if (e.isParent) {
                                e.value = e.id;
                                e.label = e.title;
                                e.loading = false;
                                e.children = [];
                            } else {
                                e.value = e.id;
                                e.label = e.title;
                            }
                        });
                        this.deleteDisableNode(res.result);
                        item.children = res.result;
                        callback();
                    }
                });
            },
            loadDataTree(item, callback) {
                loadDepartment(item.id).then(res => {
                    if (res.success === true) {
                        res.result.forEach(function (e) {
                            if (e.isParent) {
                                e.loading = false;
                                e.children = [];
                            }
                        });
                        this.deleteDisableNode(res.result);
                        callback(res.result);
                    }
                });
            },
            selectTree(v) {
                if (v.length > 0) {
                    // 转换null为""
                    for (let attr in v[0]) {
                        if (v[0][attr] === null) {
                            v[0][attr] = "";
                        }
                    }
                    let str = JSON.stringify(v[0]);
                    let data = JSON.parse(str);
                    this.userForm.departmentId = data.id;
                    this.userForm.departmentTitle = data.title;
                }
            },
            clearSelectDep() {
                this.userForm.departmentId = "";
                this.userForm.departmentTitle = "";
            },
            handleChangeDep(value, selectedData) {
                // 获取最后一个值
                if (value && value.length > 0) {
                    this.searchForm.departmentId = value[value.length - 1];
                } else {
                    this.searchForm.departmentId = "";
                }
            },
            handleChangeUserFormDep(value, selectedData) {
                // 获取最后一个值
                if (value && value.length > 0) {
                    this.userForm.departmentId = value[value.length - 1];
                } else {
                    this.userForm.departmentId = "";
                }
            },
            changePage(v) {
                this.searchForm.pageNumber = v;
                this.getUserList();
                this.clearSelectAll();
            },
            changePageSize(v) {
                this.searchForm.pageSize = v;
                this.getUserList();
            },
            selectDateRange(v) {
                if (v) {
                    this.searchForm.startDate = v[0];
                    this.searchForm.endDate = v[1];
                }
            },
            getUserList() {
                this.selectCount = 0;
                // 多条件搜索用户列表
                this.loading = true;
                getUserListData(this.searchForm).then(res => {
                    this.loading = false;
                    if (res.success === true) {
                        this.data = res.result.records;
                        this.total = res.result.total;
                    }
                });
            },
            handleSearch() {
                this.searchForm.pageNumber = 1;
                this.searchForm.pageSize = 10;
                this.getUserList();
            },
            handleSearchExportAll() {
                let str = JSON.stringify(this.searchForm);
                let params = JSON.parse(str);
                params.pageNumber = 1;
                params.pageSize = 1000000000;
                // 多条件搜索配置列表
                getUserListData(params).then(res => {
                    if (res.success === true) {
                        this.exportData = res.result.records;
                    }
                });
            },
            handleReset() {
                this.$refs.searchForm.resetFields();
                this.searchForm.pageNumber = 1;
                this.searchForm.pageSize = 10;
                this.selectDate = null;
                this.searchForm.startDate = "";
                this.searchForm.endDate = "";
                this.selectDep = [];
                this.searchForm.departmentId = "";
                // 重新加载数据
                this.getUserList();
            },
            changeSort(e) {
                this.searchForm.sort = e.key;
                this.searchForm.order = e.order;
                if (e.order === "normal") {
                    this.searchForm.order = "";
                }
                this.getUserList();
            },
            getRoleList() {
                getAllRoleList().then(res => {
                    if (res.success === true) {
                        this.roleList = res.result;
                    }
                });
            },
            initMeta() {
                let permTypes = this.$route.meta.permTypes;
                if (permTypes !== null && permTypes !== undefined) {
                    this.permTypes = permTypes;
                }
            },
            handleDropdown(name) {
                if (name === "exportData") {
                    if (this.selectCount <= 0) {
                        this.$Message.warning("您还未选择要导出的数据");
                        return;
                    }
                    this.$Modal.confirm({
                        title: "确认导出",
                        content: "您确认要导出所选 " + this.selectCount + " 条数据?",
                        onOk: () => {
                            this.$refs.exportTable.exportCsv({
                                filename: "管理员数据" + moment().format('YYYYMMDDHHmmss'),
